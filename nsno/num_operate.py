# The Numeric Operators
print(2 + 2) # Addition
print(10 - 4) # Subtraction
print(3 * 9) # Multiplication
print(5 / 3) # Division, converts the result to a float even when evenly divided.
print(5 // 3) # Floor division, always returns a number without a remainder as an int
print(8 % 3) # Modulo division, returns the remainder as an int
print(2 ** 3) # Exponent
